// Config plugin loaded as CommonJS — `@expo/config` evaluates this via Node's
// classic require, so ESM syntax / TS would not load. Disable the
// no-require-imports rule for this file only.
/* eslint-disable @typescript-eslint/no-require-imports */
const { withDangerousMod } = require("expo/config-plugins");
const fs = require("fs");
const path = require("path");

/**
 * Force modular headers for the Google C/Obj-C pods that the Swift pod
 * `AppCheckCore` depends on but which don't define module maps of their own.
 *
 * Why: `@react-native-google-signin/google-signin` pulls in GoogleSignIn →
 * AppCheckCore (a Swift pod) → GoogleUtilities + RecaptchaInterop. Under the
 * default static-library linkage (this app uses static libs + New Arch, no
 * `use_frameworks!`), CocoaPods refuses to integrate a Swift pod whose
 * dependencies lack module maps, failing `pod install` with:
 *
 *   [!] The Swift pod `AppCheckCore` depends upon `GoogleUtilities` and
 *       `RecaptchaInterop`, which do not define modules.
 *
 * The fix CocoaPods itself recommends is to opt those two pods into generating
 * module maps via `:modular_headers => true`. We do it surgically (just the two
 * named pods) rather than `use_modular_headers!` globally, which is known to
 * break the prebuilt React-Native pods this build relies on.
 *
 * Mechanism: the iOS project is generated by CNG (no committed `ios/`), so we
 * patch the generated Podfile in a dangerous mod, which runs after the base
 * Podfile is written during prebuild. Idempotent via a marker comment.
 *
 * Kept as .js (not .ts) for the same reason as with-strip-boot-completed.js:
 * @expo/config evaluates app.config.ts via a transformer that doesn't extend to
 * nested requires, so a .ts plugin import breaks `npx expo config` / EAS builds.
 */
const MARKER = "# @generated by with-modular-headers";
const POD_LINES = [
  `  ${MARKER}`,
  "  pod 'GoogleUtilities', :modular_headers => true",
  "  pod 'RecaptchaInterop', :modular_headers => true",
];

const withModularHeaders = (config) =>
  withDangerousMod(config, [
    "ios",
    (cfg) => {
      const podfile = path.join(cfg.modRequest.platformProjectRoot, "Podfile");
      let contents = fs.readFileSync(podfile, "utf8");
      if (contents.includes(MARKER)) return cfg;

      // Insert right after the main app target's opening line.
      const targetRegex = /(target ['"][^'"]+['"] do\r?\n)/;
      if (!targetRegex.test(contents)) {
        throw new Error(
          "with-modular-headers: could not find a `target '...' do` line in the Podfile",
        );
      }
      contents = contents.replace(targetRegex, `$1${POD_LINES.join("\n")}\n`);
      fs.writeFileSync(podfile, contents);
      return cfg;
    },
  ]);

module.exports = withModularHeaders;
module.exports.default = withModularHeaders;
